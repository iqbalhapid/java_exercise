public class Rokok{
    private String merk;
    private String jenis;
    private String kadar;


public Rokok (String merk, String jenis, double kadar) {
    this.merk = merk;
    this.jenis = jenis;
    this.kadar = kadar;    
}

public String getMerk(){
    return merk;
}

public String getJenis(){
    return jenis;
}

public String getKadar(){
    return kadar;
}




}